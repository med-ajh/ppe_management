            if (roleSelect.value === 'manager') {
                costCenterGroup.style.display = 'block';
            } else {
                costCenterGroup.style.display = 'none';
            }
        }

        roleSelect.addEventListener('change', toggleCostCenter);
        toggleCostCenter(); // Initial check
    });
